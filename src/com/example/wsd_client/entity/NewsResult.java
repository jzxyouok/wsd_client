package com.example.wsd_client.entity;

/**
 * 封装消息内容的实体类
 */
public class NewsResult {

	/**
	 * 下单日期
	 */
	private String InputDate;
	/**
	 * 消息类别
	 */
	private int Status;
	
	private int InputOwner;
	private String SendPhone;
	/**
	 * 客户id
	 */
	private int ClientID;
	private int DeptID;
	/**
	 * 消息编号
	 */
	private int MessageID;
	/**
	 * 消息内容
	 */
	private String MessageInfo;
	
	
	public String getInputDate() {
		return InputDate;
	}
	public void setInputDate(String inputDate) {
		InputDate = inputDate;
	}
	public int getStatus() {
		return Status;
	}
	public void setStatus(int status) {
		Status = status;
	}
	public int getInputOwner() {
		return InputOwner;
	}
	public void setInputOwner(int inputOwner) {
		InputOwner = inputOwner;
	}
	public String getSendPhone() {
		return SendPhone;
	}
	public void setSendPhone(String sendPhone) {
		SendPhone = sendPhone;
	}
	public int getClientID() {
		return ClientID;
	}
	public void setClientID(int clientID) {
		ClientID = clientID;
	}
	public int getDeptID() {
		return DeptID;
	}
	public void setDeptID(int deptID) {
		DeptID = deptID;
	}
	public int getMessageID() {
		return MessageID;
	}
	public void setMessageID(int messageID) {
		MessageID = messageID;
	}
	public String getMessageInfo() {
		return MessageInfo;
	}
	public void setMessageInfo(String messageInfo) {
		MessageInfo = messageInfo;
	}
	@Override
	public String toString() {
		return "NewsResult [InputDate=" + InputDate + ", Status=" + Status
				+ ", InputOwner=" + InputOwner + ", SendPhone=" + SendPhone
				+ ", ClientID=" + ClientID + ", DeptID=" + DeptID
				+ ", MessageID=" + MessageID + ", MessageInfo=" + MessageInfo
				+ "]";
	}
	
}
