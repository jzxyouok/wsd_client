package com.example.wsd_client.entity;

import java.io.Serializable;

/**
 * 实体类，封装产品的所有属性
 * @author wsd_leiguoqiang
 */
public class Product implements Serializable{
	/**
	 * 版本号
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * ID编号
	 */
	private int YWM_ID;
	/**
	 * 品种代码
	 */
	private String YWM_Code;
	/**
	 * 品种名称
	 */
	private String YWM_Name;
	/**
	 * 单位
	 */
	private String YWM_Unit;
	/**
	 * 种类（散装/袋装）
	 */
	private String YWM_Kind;
	/**
	 * 该品种有效期
	 */
	private String YWM_Validate;
	/**
	 * 建立日期
	 */
	private String YWM_CDate;
	/**
	 * 单价
	 */
	private String YWM_Price;
	/**
	 * 图片名称
	 */
	private String YWM_PicName;
	
	public String getYWM_PicName() {
		return YWM_PicName;
	}
	public void setYWM_PicName(String yWM_PicName) {
		YWM_PicName = yWM_PicName;
	}
	public int getYWM_ID() {
		return YWM_ID;
	}
	public void setYWM_ID(int yWM_ID) {
		YWM_ID = yWM_ID;
	}
	public String getYWM_Code() {
		return YWM_Code;
	}
	public void setYWM_Code(String yWM_Code) {
		YWM_Code = yWM_Code;
	}
	public String getYWM_Name() {
		return YWM_Name;
	}
	public void setYWM_Name(String yWM_Name) {
		YWM_Name = yWM_Name;
	}
	public String getYWM_Unit() {
		return YWM_Unit;
	}
	public void setYWM_Unit(String yWM_Unit) {
		YWM_Unit = yWM_Unit;
	}
	public String getYWM_Kind() {
		return YWM_Kind;
	}
	public void setYWM_Kind(String yWM_Kind) {
		YWM_Kind = yWM_Kind;
	}
	public String getYWM_Validate() {
		return YWM_Validate;
	}
	public void setYWM_Validate(String yWM_Validate) {
		YWM_Validate = yWM_Validate;
	}
	public String getYWM_CDate() {
		return YWM_CDate;
	}
	public void setYWM_CDate(String yWM_CDate) {
		YWM_CDate = yWM_CDate;
	}
	public String getYWM_Price() {
		return YWM_Price;
	}
	public void setYWM_Price(String yWM_Price) {
		YWM_Price = yWM_Price;
	}
	@Override
	public String toString() {
		return "Product [YWM_ID=" + YWM_ID + ", YWM_Code=" + YWM_Code
				+ ", YWM_Name=" + YWM_Name + ", YWM_Unit=" + YWM_Unit
				+ ", YWM_Kind=" + YWM_Kind + ", YWM_Validate=" + YWM_Validate
				+ ", YWM_CDate=" + YWM_CDate + ", YWM_Price=" + YWM_Price
				+ ", YWM_PicName=" + YWM_PicName + "]";
	}
}

